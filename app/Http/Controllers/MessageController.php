    }

    public function store(Request $request, Conversation $conversation)
    {
        $request->validate([
            'message' => 'required'
        ]);
        Message::create([
            'user_id' => $request->user()->id,
            'conversation_id' => $conversation->id,
            'message' => $request->message
        ]);

        return back();
    }

